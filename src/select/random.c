#include "common.h"

#define RANDOM_MAX  255

extern u8 random_array_seed[64] = {
    0x9A, 0xF9, 0x93, 0xDD, 0xF8, 0xFE, 0x85, 0x4B,
    0xE2, 0xA5, 0x92, 0xC9, 0x4C, 0x22, 0x51, 0x61,
    0xFA, 0x4E, 0x37, 0x23, 0x91, 0x7C, 0x0E, 0x57,
    0xA8, 0x2B, 0x2A, 0x98, 0x49, 0x60, 0x7D, 0x3F,
    0xB7, 0xBC, 0x0B, 0x63, 0xD0, 0xAF, 0xAD, 0x9D,
    0x53, 0x0A, 0x4A, 0x4F, 0x05, 0x6D, 0x0F, 0x09,
    0x70, 0xC8, 0x52, 0x5A, 0x74, 0xA2, 0x6E, 0xFF,
    0x08, 0xE5, 0xC3, 0x75, 0xCD, 0xBB, 0xB0, 0xF4
};

u8 random_array[64];
u32 random_counter = 0;

void random_init(void)
{
    random_counter = 0;
    for (int i = 0; i < 64; i++)
        random_array[i] = random_array_seed[i];
}

s32 random_byte(void)
{
    u8 ret;
    random_counter = (random_counter + 1) % 64;
    
    ret = random_array[(random_counter - 24) % 64]
        ^ random_array[(random_counter - 55) % 64];
    random_array[random_counter] = ret;
    return ret;
}

s32 random_range(s32 a, s32 b)
{
    s32 width = b - a;
    return a + ((random_byte() * width) / RANDOM_MAX) % width;
}